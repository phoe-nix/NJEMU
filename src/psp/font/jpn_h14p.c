int jpn_h14p_get_gryph(struct font_t *font, UINT16 code)
{
	if (code < NUM_FONTS)
	{
		font->data   = &jpn_h14p[jpn_h14p_pos[code]];
		font->width  = jpn_h14p_width[code];
		font->height = jpn_h14p_height[code];
		font->pitch  = jpn_h14p_pitch[code];
		font->skipx  = jpn_h14p_skipx[code];
		font->skipy  = jpn_h14p_skipy[code];
		return 1;
	}
	return 0;
}

int jpn_h14p_get_pitch(UINT16 code)
{
	if (code < NUM_FONTS)
		return jpn_h14p_pitch[code];

	return jpn_h14p_pitch[0];
}
